num1 = int(input("Enter first number: ")) #str -> int is called Type Casting
num2 = int(input("Enter second number: "))

print("datatype of first number is ", type(num1))
print(type(num2))

sum_of_num = num1 + num2
print(sum_of_num)

diff_of_num = num1 - num2
print("diff of two numbers is ",diff_of_num)

product_of_num = num1 * num2
print("Product of two numbers is ",product_of_num)

division_of_num = num1 / num2
print("division of two numbers is ",division_of_num)